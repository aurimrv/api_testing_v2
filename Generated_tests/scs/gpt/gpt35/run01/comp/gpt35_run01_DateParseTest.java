public class gpt35_run01_DateParseTest {

    private static final SutHandler controller = new em.embedded.org.restscs.EmbeddedEvoMasterController();
    private static String baseUrlOfSut;

    @BeforeClass
    public static void initClass() {
        controller.setupForGeneratedTest();
        baseUrlOfSut = controller.startSut();
        controller.registerOrExecuteInitSqlCommandsIfNeeded();
        assertNotNull(baseUrlOfSut);
        RestAssured.enableLoggingOfRequestAndResponseIfValidationFails();
        RestAssured.useRelaxedHTTPSValidation();
        RestAssured.urlEncodingEnabled = false;
        RestAssured.config = RestAssured.config()
            .jsonConfig(JsonConfig.jsonConfig().numberReturnType(JsonPathConfig.NumberReturnType.DOUBLE))
            .redirect(redirectConfig().followRedirects(false));
    }

    @AfterClass
    public static void tearDown() {
        controller.stopSut();
    }

    @Before
    public void initTest() {
        controller.resetStateOfSUT();
    }

    // Test constructor to ensure it's executed
    @Test
    public void testConstructor() {
        new org.restscs.imp.DateParse();
    }

    @Test
    public void testDateParse() {
        // Test different combinations of day names and month names
        assertEquals("1", org.restscs.imp.DateParse.subject("mon", "jan"));
        assertEquals("7", org.restscs.imp.DateParse.subject("sun", "jul"));
        assertEquals("12", org.restscs.imp.DateParse.subject("wed", "dec"));
        
        // Test lowercase and uppercase day and month names
        assertEquals("4", org.restscs.imp.DateParse.subject("ThUr", "AUG"));
        assertEquals("9", org.restscs.imp.DateParse.subject("fri", "SEP"));
        
        // Test unknown day and month names
        assertEquals("0", org.restscs.imp.DateParse.subject("xyz", "abc"));
    }
}
